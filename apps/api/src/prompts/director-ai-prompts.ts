export function buildDirectorReplyUserSales(payload: {
  teamMemberName: string;
  reportTitle: string;
  reportBody: string;
  submittedAtIso: string;
}): string {
  return [
    `Team member: ${payload.teamMemberName} (sales)`,
    `Report title: ${payload.reportTitle}`,
    `Submitted at (ISO): ${payload.submittedAtIso}`,
    `Project name: (from title/body only — sales reports do not carry a separate project record)`,
    `Phase / milestone / deadline: infer only if explicitly stated in the report; otherwise say "Not stated."`,
    `Report content:\n${payload.reportBody}`,
    `Blockers flagged: infer from the report text if any; otherwise "None stated."`,
    `Previous remarks: none (thread not included)`,
    "",
    "Read this report and respond as the Director. Follow the system rules exactly."
  ].join("\n");
}

export function buildDirectorReplyUserDeveloper(payload: {
  teamMemberName: string;
  reportDateIso: string;
  whatWorked: string | null;
  blockers: string | null;
  needsAttention: string | null;
  implemented: string | null;
  pending: string | null;
  nextPlan: string | null;
}): string {
  const blockers = payload.blockers?.trim() || "None stated.";
  return [
    `Team member: ${payload.teamMemberName} (developer)`,
    `Report day (date): ${payload.reportDateIso}`,
    `What worked: ${payload.whatWorked ?? "—"}`,
    `Implemented / shipped: ${payload.implemented ?? "—"}`,
    `Pending: ${payload.pending ?? "—"}`,
    `Next plan: ${payload.nextPlan ?? "—"}`,
    `Needs attention: ${payload.needsAttention ?? "—"}`,
    `Blockers: ${blockers}`,
    `Project / milestone / deadline: infer only if explicitly mentioned above; otherwise "Not stated."`,
    `Previous remarks: none`,
    "",
    "Read this report and respond as the Director. Follow the system rules exactly."
  ].join("\n");
}

export function buildDirectorBriefingUser(payloadJson: string): string {
  return `Use the following JSON only as your source of truth. Produce the briefing in the exact structure from the system prompt.\n\n${payloadJson}`;
}
